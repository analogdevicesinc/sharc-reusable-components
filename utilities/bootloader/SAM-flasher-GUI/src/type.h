#ifndef __TYPE_H__
#define __TYPE_H__

#include <stdint.h>

// signed and unsigned 8, 16 and 32 bit types
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef int8_t s8;
typedef int16_t s16;
typedef int32_t s32;

#endif
